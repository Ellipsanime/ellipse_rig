def bounding_box_utils(objRef,size=None,delete=False):
    if size ==None:
        size =(1,1,1)
    dicBound = {}
    if objRef != []: # check if objRef is empty
        if type(objRef) == type([]): # check if objRef is a list
            pass
        else:
            objRef=[objRef]
        # get position of multi Object ###
        bbox = mc.exactWorldBoundingBox(objRef, calculateExactly=True)
        x1, y1, z1, x2, y2, z2 = mc.exactWorldBoundingBox(objRef, calculateExactly=True)
        valTrans = [(x1 + x2) / 2, (y1 + y2) / 2, (z1 + z2) / 2]
        cubeBoundingBox = mc.polyCube(n="tamp")[0]
        mc.xform(cubeBoundingBox, t=valTrans, ws=True)
        dicBound["transBound"] = mc.xform(cubeBoundingBox, q=True, t=True)
        mc.move(x1, '%s.f[5]' % cubeBoundingBox, x=True)
        mc.move(y1, '%s.f[3]' % cubeBoundingBox, y=True)
        mc.move(z1, '%s.f[2]' % cubeBoundingBox, z=True)
        mc.move(x2, '%s.f[4]' % cubeBoundingBox, x=True)
        mc.move(y2, '%s.f[1]' % cubeBoundingBox, y=True)
        mc.move(z2, '%s.f[0]' % cubeBoundingBox, z=True)
        dicBound["sizeX"] = float((mc.getAttr(cubeBoundingBox + ".boundingBoxSizeX")/(mc.getAttr(objRef[0]+ ".scaleX")))*size[0])
        dicBound["sizeY"] = float((mc.getAttr(cubeBoundingBox + ".boundingBoxSizeY")/(mc.getAttr(objRef[0]+ ".scaleY")))*size[1])
