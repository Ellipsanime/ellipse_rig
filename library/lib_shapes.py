def side_color(side=None):
    dic ={}
    dic["colorMaster"] = "brown24"
    dic["colorIk"] = "red4"
    dic["colorCtr"] = "yellow"
    if side == 'L':
        dic["colorMaster"] = "red"
        dic["colorIk"] = "red4"
        dic["colorCtr"] = "pink31"
    elif side == 'R':
        dic["colorMaster"] = "blue"
        dic["colorIk"] = "blue5"
        dic["colorCtr"] = "blue15"
    return dic




